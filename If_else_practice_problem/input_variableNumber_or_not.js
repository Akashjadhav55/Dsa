let x = 10;
let y = 3;

console.log("Addition: " + (x + y));          // 13
console.log("Subtraction: " + (x - y));       // 7
console.log("Multiplication: " + (x * y));    // 30
console.log("Division: " + (x / y));          // 3.3333333333333335
console.log("Modulus: " + (x % y));           // 1
console.log("Exponentiation: " + (x ** y));   // 1000

x++;  // Increment x by 1
